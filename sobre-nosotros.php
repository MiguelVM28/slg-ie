                <p>
                    El Colegio Nacional “San Luis Gonzaga” de Ica tiene sus raíces en el legado del Capitán español Don Antonio de Vargas y Medrano y su esposa Doña Leandra de Tardío, quienes al no tener herederos, decidieron el 24 de junio de 1691 donar todos sus bienes para la fundación de un colegio en Ica, dirigido por la Compañía de Jesús. Esta voluntad se ejecutó el 30 de octubre de 1693 gracias al hermano de Doña Leandra, el padre jesuita Hernando de Tardío, quien actuó como albacea.
                </p>
                <p>
                    La Compañía de Jesús, fundada por San Ignacio de Loyola, era reconocida por su estricta organización y su alto compromiso con la educación, guiada por el Ratio Studiorum. En 1746, mediante una Real Cédula, el Rey de España autorizó formalmente la fundación del colegio en Ica. Así, el 21 de junio de 1748, se inauguró oficialmente el Colegio de los Jesuitas, posiblemente ya bajo el nombre “San Luis Gonzaga”, en honor al joven santo canonizado también un 21 de junio.
                </p>
